#include <iostream>
using namespace std;
// This program demonstrates the use of variables and comments in C++.
// and created by Kaushal karn

int main() {
    int a = 10; // This is a single line comment
    float b = 20.01; /* This is a multi-line comment
                   that spans multiple lines */
    cout << "The value of a is: " << a << endl;
    cout << "The value of b is: " << b << endl;

    return 0;
}
