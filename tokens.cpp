#include<iostream>
using namespace std;

int main() {

    int number = 10;    // keyword + identifier + operator + literal
    float pi = 3.14;   // keyword + identifier + operator + literal
    char grade = 'A';  // character literal
    string name = "kaushal"; // string literal

    number = number + 5; // operator + identifier + literal

    cout << number << endl;
    cout << pi << endl;
    cout << grade << endl;
    cout << name << endl;

    cout << "Hello, World!" << endl; // string literal

    return 0;
}

